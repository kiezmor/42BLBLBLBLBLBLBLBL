#include "libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	int v;

	v = 0;
	while (s1[v] == s2[v] && s1[v] != '\0' && s2[v] != '\0')
		v++;
	return (s1[v] - s2[v]);
}
