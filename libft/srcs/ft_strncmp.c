#include "libft.h"

int		ft_strncmp(char *s1, char *s2, size_t n)
{
	size_t	v;

	v = 0;
	while (s1[v] == s2[v] && s1[v] != '\0' && s2[v] != '\0' && v <= n)
		v++;
	return (s1[v] - s2[v]);
}
